#!/usr/bin/env bash
# XCloak Security Suite вҖ” one-command installer
# Usage: curl -fsSL https://raw.githubusercontent.com/The-Abhishek1/XCLOAK-SECURITY-SUITE/main/install.sh | bash
set -euo pipefail

REPO="https://github.com/The-Abhishek1/XCLOAK-SECURITY-SUITE.git"
DIR="XCLOAK-SECURITY-SUITE"
COMPOSE_FILE="docker-compose.quickstart.yml"

# в”Җв”Җ colours в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
RED='\033[0;31m'; GREEN='\033[0;32m'; YELLOW='\033[1;33m'
CYAN='\033[0;36m'; BOLD='\033[1m'; RESET='\033[0m'

info()    { echo -e "${CYAN}[xcloak]${RESET} $*"; }
success() { echo -e "${GREEN}[xcloak]${RESET} $*"; }
warn()    { echo -e "${YELLOW}[xcloak]${RESET} $*"; }
die()     { echo -e "${RED}[xcloak] ERROR:${RESET} $*" >&2; exit 1; }

# в”Җв”Җ banner в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
echo -e "${BOLD}"
cat <<'EOF'
 в–Ҳв–Ҳв•—  в–Ҳв–Ҳв•— в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—в–Ҳв–Ҳв•—      в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—  в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•— в–Ҳв–Ҳв•—  в–Ҳв–Ҳв•—
 в•ҡв–Ҳв–Ҳв•—в–Ҳв–Ҳв•”в•қв–Ҳв–Ҳв•”в•җв•җв•җв•җв•қв–Ҳв–Ҳв•‘     в–Ҳв–Ҳв•”в•җв•җв•җв–Ҳв–Ҳв•—в–Ҳв–Ҳв•”в•җв•җв–Ҳв–Ҳв•—в–Ҳв–Ҳв•‘ в–Ҳв–Ҳв•”в•қ
  в•ҡв–Ҳв–Ҳв–Ҳв•”в•қ в–Ҳв–Ҳв•‘     в–Ҳв–Ҳв•‘     в–Ҳв–Ҳв•‘   в–Ҳв–Ҳв•‘в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•‘в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•”в•қ
  в–Ҳв–Ҳв•”в–Ҳв–Ҳв•— в–Ҳв–Ҳв•‘     в–Ҳв–Ҳв•‘     в–Ҳв–Ҳв•‘   в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•”в•җв•җв–Ҳв–Ҳв•‘в–Ҳв–Ҳв•”в•җв–Ҳв–Ҳв•—
 в–Ҳв–Ҳв•”в•қ в–Ҳв–Ҳв•—в•ҡв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—в•ҡв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•”в•қв–Ҳв–Ҳв•‘  в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•‘  в–Ҳв–Ҳв•—
 в•ҡв•җв•қ  в•ҡв•җв•қ в•ҡв•җв•җв•җв•җв•җв•қв•ҡв•җв•җв•җв•җв•җв•җв•қ в•ҡв•җв•җв•җв•җв•җв•қ в•ҡв•җв•қ  в•ҡв•җв•қв•ҡв•җв•қ  в•ҡв•җв•қ
 Security Suite вҖ” Open Source SOC Platform
EOF
echo -e "${RESET}"

# в”Җв”Җ requirements в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
info "Checking requirements..."

command -v docker  >/dev/null 2>&1 || die "Docker is not installed. Install it from https://docs.docker.com/get-docker/"
command -v git     >/dev/null 2>&1 || die "git is not installed."

# Docker Compose v2 (plugin) or v1 (standalone)
if docker compose version >/dev/null 2>&1; then
  COMPOSE="docker compose"
elif command -v docker-compose >/dev/null 2>&1; then
  COMPOSE="docker-compose"
else
  die "Docker Compose is not installed. Install it from https://docs.docker.com/compose/install/"
fi

# Check Docker daemon is running
docker info >/dev/null 2>&1 || die "Docker daemon is not running. Start Docker and try again."

success "Requirements satisfied."

# в”Җв”Җ system resources warning в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
TOTAL_MEM_KB=$(grep MemTotal /proc/meminfo 2>/dev/null | awk '{print $2}' || echo 0)
if [ "$TOTAL_MEM_KB" -gt 0 ] && [ "$TOTAL_MEM_KB" -lt 3145728 ]; then
  warn "Less than 3 GB RAM detected. XCloak may run slowly. 4 GB+ recommended."
fi

DISK_FREE_KB=$(df -k . | awk 'NR==2{print $4}' || echo 0)
if [ "$DISK_FREE_KB" -gt 0 ] && [ "$DISK_FREE_KB" -lt 10485760 ]; then
  warn "Less than 10 GB free disk space. Docker images need ~4 GB."
fi

# в”Җв”Җ clone or update в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
if [ -d "$DIR/.git" ]; then
  info "Repository already exists вҖ” pulling latest..."
  git -C "$DIR" pull --ff-only
elif [ -f "docker-compose.quickstart.yml" ]; then
  info "Running inside the repository вҖ” skipping clone."
  DIR="."
else
  info "Cloning XCloak..."
  git clone --depth 1 "$REPO" "$DIR"
fi

cd "$DIR"

# в”Җв”Җ secrets в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
# Generate secure secrets if they haven't been set already. These are baked
# into the quickstart compose via env vars вҖ” change them before exposing the
# stack to the internet.
JWT_SECRET="${JWT_SECRET:-$(openssl rand -hex 32 2>/dev/null || head -c 32 /dev/urandom | xxd -p | tr -d '\n')}"
METRICS_TOKEN="${METRICS_TOKEN:-$(openssl rand -hex 16 2>/dev/null || head -c 16 /dev/urandom | xxd -p | tr -d '\n')}"

# Patch the quickstart compose with the generated secrets (sed in-place copy)
PATCHED_COMPOSE=$(mktemp)
sed \
  -e "s|quickstart-jwt-secret-replace-in-production|${JWT_SECRET}|g" \
  -e "s|quickstart-metrics-token|${METRICS_TOKEN}|g" \
  "$COMPOSE_FILE" > "$PATCHED_COMPOSE"

# в”Җв”Җ pull base images first so progress is visible в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
info "Pulling base images..."
$COMPOSE -f "$PATCHED_COMPOSE" pull --ignore-buildable 2>/dev/null || true

# в”Җв”Җ build & start в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
info "Building and starting XCloak (this takes ~3 minutes on first run)..."
$COMPOSE -f "$PATCHED_COMPOSE" up -d --build

rm -f "$PATCHED_COMPOSE"

# в”Җв”Җ wait for healthy в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
info "Waiting for backend to be ready..."
TIMEOUT=120
ELAPSED=0
until curl -sf http://localhost:8080/api/health >/dev/null 2>&1; do
  if [ "$ELAPSED" -ge "$TIMEOUT" ]; then
    die "Backend did not start within ${TIMEOUT}s. Check logs: docker compose -f $COMPOSE_FILE logs backend"
  fi
  sleep 3
  ELAPSED=$((ELAPSED + 3))
  echo -n "."
done
echo ""

success "XCloak is running!"
echo ""
echo -e "${BOLD}в”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғ${RESET}"
echo -e "  ${GREEN}Dashboard${RESET}   вҶ’  ${BOLD}http://localhost:3000${RESET}"
echo -e "  ${GREEN}Backend API${RESET} вҶ’  http://localhost:8080/api/health"
echo ""
echo -e "  Create your admin account on first visit."
echo -e "${BOLD}в”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғ${RESET}"
echo ""
echo -e "  ${YELLOW}Enroll an agent:${RESET}"
echo -e "  curl -fsSL https://raw.githubusercontent.com/The-Abhishek1/XCLOAK-SECURITY-SUITE/main/scripts/enroll-agent.sh | bash"
echo ""
echo -e "  ${YELLOW}Stop XCloak:${RESET}"
echo -e "  docker compose -f $COMPOSE_FILE down"
echo ""
echo -e "  Docs вҶ’ ${CYAN}https://docs.xcloak.tech${RESET}"
echo -e "  Repo вҶ’ ${CYAN}https://github.com/The-Abhishek1/XCLOAK-SECURITY-SUITE${RESET}"
echo ""
